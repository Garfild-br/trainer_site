
//-------анимация для иконок при скролле 1-------//

let anime_for_icon_box = window.scrollY

const box_icon = document.querySelector('.container_1_icon')
const scroll_box = 260;

const add_class_at_icon = () =>
    box_icon.classList.add('for_cont2')

const remove_class_at_icon = () =>
    box_icon.classList.remove('for_cont2')

window.addEventListener('scroll', function () {
    anime_for_icon_box = window.scrollY;

    if (anime_for_icon_box >= scroll_box) {
        add_class_at_icon()
    }
    else {
        remove_class_at_icon()
    }
})

